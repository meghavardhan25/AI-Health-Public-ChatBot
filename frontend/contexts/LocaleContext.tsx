const STORAGE_KEY = "hb_locale";

function normalizeLocale(code: string | null | undefined): AppLocale {
  const c = (code ?? "en").toLowerCase().slice(0, 2);
  if (Object.prototype.hasOwnProperty.call(STRINGS, c)) return c as AppLocale;
  return "en";
}

type LocaleCtx = {
  locale: AppLocale;
  setLocale: (l: AppLocale) => void;
  t: (key: string) => string;
  supported: typeof SUPPORTED_LOCALES;
};

const LocaleContext = createContext<LocaleCtx | null>(null);

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<AppLocale>("en");

  useEffect(() => {
    const stored = normalizeLocale(localStorage.getItem(STORAGE_KEY));
    setLocaleState(stored);
  }, []);

  const setLocale = useCallback((l: AppLocale) => {
    setLocaleState(l);
    if (typeof window !== "undefined") localStorage.setItem(STORAGE_KEY, l);
  }, []);

  const t = useCallback(
    (key: string) => {
      const table = STRINGS[locale] ?? STRINGS.en;
      return table[key] ?? STRINGS.en[key] ?? key;
    },
    [locale],
  );

  const value = useMemo(
    () => ({ locale, setLocale, t, supported: SUPPORTED_LOCALES }),
    [locale, setLocale, t],
  );

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const ctx = useContext(LocaleContext);
  if (!ctx) throw new Error("useLocale must be used inside LocaleProvider");
  return ctx;
}
